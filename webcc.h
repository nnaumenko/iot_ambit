//////////////////////////////////////////////////////////////////////
// HTTPRequestPart
//////////////////////////////////////////////////////////////////////

/// Parts of the HTTP request which are passed from HTTP Request Parser

enum class HTTPRequestPart {
  NONE,               ///< No request part passed by parser
  METHOD,             ///< Method from request header (e.g. GET or POST)
  PATH,               ///< Path from request header (e.g. /index.html)
  URL_QUERY_NAME,     ///< Name from the URL query string item (e.g. name1 or name2 for URL /index?name1=value1&name2=value2)
  URL_QUERY_VALUE,    ///< Value from the URL query string item (e.g. value1 or value2 for URL /index?name1=value1&name2=value2)
  HTTP_VERSION,       ///< HTTP version from request header (e.g. HTTP/1.1)
  FIELD_NAME,         ///< Name of the header field (e.g. Field-Name for field Field-Name:value1=value2;value3=value4)
  FIELD_VALUE_PART1,  ///< First part of the header field value (e.g. value1 or value3 for field Field-Name:value1=value2;value3=value4)
  FIELD_VALUE_PART2,  ///< Second part of the header field value (e.g. value2 or value4 for field Field-Name:value1=value2;value3=value4)
  POST_QUERY_NAME,    ///< Name from the POST query string item (e.g. name1 or name2 for POST request body name1=value1&name2=value2)
  POST_QUERY_VALUE,   ///< Value from the POST query string item (e.g. value1 or value2 for POST request body name1=value1&name2=value2)
};

//////////////////////////////////////////////////////////////////////
// ParseError
//////////////////////////////////////////////////////////////////////

/// Errors generated by HTTP request parser

enum class ParseError {
  NONE,                   ///< No error
  INTERNAL_ERROR,         ///< Parser's internal error
  REQUEST_PART_TOO_LONG,  ///< Request part is too long to fit into buffer
  REQUEST_STRUCTURE,      ///< Malformed request
  REQUEST_SEMANTICS       ///< Request information is semantically invalid
};

