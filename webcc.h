    case ParseError::REQUEST_PART_TOO_LONG:
      return (FPSTR(textsUI.parseErrorRequestPartTooLong));
    case ParseError::REQUEST_STRUCTURE:
      return (FPSTR(textsUI.parseErrorRequestStructure));
