/// @brief Sends string from RAM or PROGMEM to the client, if the string is NULL, prints defaultString
/// @param string CString in RAM or PROGMEM to be printed
/// @param progmemString If true string is assumed to be located in PROGMEM,
/// if false sting is assumed to be located in RAM
/// @param defaultString This CString is printed if string parameters is NULL.
/// This CString must always be located in PROGMEM
void WebccForm::print(const char * string, boolean progmemString, const __FlashStringHelper * defaultString) {
  if (!string) {
    if (defaultString) client->print(defaultString);
    return;
  }
  progmemString ? client->print(FPSTR(string)) : client->print(string);
}

/// @brief Generates parameter with drop-down list
/// @tparam Options parameter pack containing pairs of value and name
/// (e.g. <1, "MyValue">) for drop-down list items. See also selectParameterExpand()
/// @param displayName CString which contains parameter's name visible for user in
/// the UI. Can be located in RAM or PROGMEM (depending on value of progmemStrings
/// parameter)
/// @param internalName CString which contains parameter's name referred internally
/// in POST-requests, etc. Can be located in RAM or PROGMEM (depending on value of
/// progmemStrings parameter)
/// @param value Drop-down list parameter's current value (currently selected item)
/// @param tooltipText CString which contains parameter's tooltip text (or NULL if
/// no tooltip required). Can be located in RAM or PROGMEM (depending on value of
/// progmemStrings parameter).
/// @param progmemStrings If true cstring arguments displayName, internalName and
/// tooltipText are assumed to be located in PROGMEM, if false these cstring
/// arguments are assumed to be located in RAM
/// @param options Contains pairs of value and name, e.g. (1, "MyValue") of
/// drop-down list items. See also selectParameterExpand()

template <class... Options>
void WebccForm::selectParameter(const char * displayName,
                                const char * internalName,
                                long value,
                                const char * tooltipText,
                                boolean progmemStrings,
                                Options... options) {
  selectParameterBegin(displayName, internalName, tooltipText, progmemStrings);
  selectParameterExpand(value, progmemStrings, options...);
  selectParameterEnd();
}

