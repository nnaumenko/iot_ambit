void WebccForm::plaintext (const char * displayName,
                           long value,
                           const char * tooltipText,
                           boolean progmemStrings) {
  const size_t MAX_CHARS_PER_LONG = 12; //max 10 digits per long + optional minus sign + null character
  const int RADIX_DECIMAL = 10;
  char valueAsText[MAX_CHARS_PER_LONG] = {0};
  ltoa (value, valueAsText, RADIX_DECIMAL);
  plaintext(displayName, valueAsText, tooltipText, progmemStrings);
}

/// @brief Output drop-down list parameter's beginning HTML code
void WebccForm::selectParameterBegin(const char * displayName,
                                     const char * internalName,
                                     const char * tooltipText,
                                     boolean progmemStrings) {
  if (!client) return;
  client->print(FPSTR(webccFormHTML.selectParameter1));
  print(displayName, progmemStrings);
  client->print(FPSTR(webccFormHTML.selectParameter2));
  tooltip(tooltipText, progmemStrings);
  client->print(FPSTR(webccFormHTML.selectParameter3));
  print(internalName, progmemStrings);
  client->print(FPSTR(webccFormHTML.selectParameter4));
}

/// @brief Output a single list item HTML code of drop-down
/// list parameter
void WebccForm::selectParameterOption(const char * displayName,
                                      long optionValue,
                                      long actualValue,
                                      boolean progmemStrings) {
  if (!client) return;
  client->print(FPSTR(webccFormHTML.selectParameterOption1));
  client->print(optionValue, DEC);
  if (optionValue == actualValue)
    client->print(FPSTR(webccFormHTML.selectParameterOption2Selected));
  else
    client->print(FPSTR(webccFormHTML.selectParameterOption2));
  print(displayName, progmemStrings);
  client->print(FPSTR(webccFormHTML.selectParameterOption3));
}


/// @brief Output drop-down list parameter's ending HTML code
void WebccForm::selectParameterEnd (void) {
  if (!client) return;
  client->print(FPSTR(webccFormHTML.selectParameter5));
}

/// @brief Output a tooltip to HTML form
/// @param tooltipText Cstring, text of the tooltip or NULL if
/// tooltip not required
/// @param progmemStrings If true then tooltipText cstring is
/// assumed to be located in PROGMEM, if false then tooltipText
/// cstring assumed to be located in RAM
void WebccForm::tooltip(const char * tooltipText, boolean progmemStrings) {
  if (!client) return;
  if (!tooltipText) return;
  client->print(FPSTR(webccFormHTML.tooltipBegin));
  print(tooltipText, progmemStrings);
  client->print(FPSTR(webccFormHTML.tooltipEnd));
}

